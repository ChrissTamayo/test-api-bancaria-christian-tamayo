create table detalles_movimiento (movimiento_id integer not null auto_increment, fecha date, saldo double precision, tipo_movimiento varchar(255), valor double precision, cuenta_cuentaid integer, cuenta_estadoid bit, cuenta_tipo_cuenta varchar(255), primary key (movimiento_id));
create table detalles_transaccion (identificacion_transaccion integer not null auto_increment, cuenta_destino varchar(255), cuenta_origen varchar(255), estado_cuenta bit not null, limite_diario_retiro double precision not null, monto varchar(255), num_identificacion_cliente integer not null, num_identificacion_cuenta integer not null, num_identificacion_movimiento integer not null, saldo_disponible varchar(255), tipo_cuenta varchar(255), tipo_movimiento varchar(255), primary key (identificacion_transaccion));
create table detalles_cliente (contrasena varchar(255) not null, estado boolean, persona_id integer not null, primary key (persona_id));
create table detalles_cuenta (cuentaid integer not null, estadoid boolean not null, tipo_cuenta varchar(255) not null, numero_cuenta varchar(255) not null, saldo_inicial double not null, cliente_persona_id integer, primary key (cuentaid, estadoid, tipo_cuenta));
create table detalles_persona (persona_id integer generated by default as identity, direccion varchar(255), edad integer not null, genero varchar(255) not null, identificacion varchar(255) not null, nombre varchar(255) not null, telefono varchar(255), primary key (persona_id))


